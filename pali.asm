;palindrome (input in code itself)
.model small
.data
	MYSTRING DB 'MALAYALAM$'
	STRING1 DB 'PALINDROME$'
	STRING2 DB ' NOT PALINDROME$'
.code
	MOV AX,@DATA
	MOV DS,AX
	LEA DI,MYSTRING
	MOV SI,DI
	BACK:MOV AL,[DI]
		CMP AL,'$'
		JZ SCAN
		INC DI
		JMP BACK
	SCAN:DEC DI
	CMP SI,DI
	JNC OUT1
	MOV AL,[SI]
	CMP AL,[DI]
	JNZ OUT2
	INC SI
	JMP SCAN
OUT1:LEA DX,STRING1
JMP L2
OUT2: LEA DX,STRING2
L2:MOV AH,09H
	INT 21H
	MOV AH,4CH
	INT 21H

END
